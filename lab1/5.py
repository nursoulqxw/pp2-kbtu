#Data Types
x = list(("apple", "banana", "cherry"))

#display x:
print(x)

#display the data type of x:
print(type(x)) 

x1 = tuple(("apple", "banana", "cherry"))

#display x:
print(x1)

#display the data type of x:
print(type(x1)) 

x2 = dict(name="John", age=36)

#display x:
print(x2)

#display the data type of x:
print(type(x2)) 

x3 = set(("apple", "banana", "cherry"))

#display x:
print(x3)

#display the data type of x:
print(type(x3)) 


x4 = bool(5)

#display x:
print(x4)

#display the data type of x:
print(type(x4)) 
